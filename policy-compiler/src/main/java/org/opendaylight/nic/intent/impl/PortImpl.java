package org.opendaylight.nic.intent.impl;

import org.opendaylight.nic.common.Port;

public class PortImpl implements Port {

	private Integer value;

	@Override
	public Integer value() {
		return value;
	}

	public PortImpl(Integer value) {
		super();
		this.value = value;
	}
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PortImpl other = (PortImpl) obj;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "OdlPortImpl [value=" + value + "]";
	}


}
